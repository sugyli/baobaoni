女工却故意绊了她胳膊一下，她又一个趔趄倒了下去。</p><p>阿玲才不管她痛不痛，一脚蹿翻她的行李箱，任由东西散落一地。</p><p>即便之前受过的委屈再大，盛夏也扛了下来，一直隐忍。可是这一刻，看着自己所剩无几的随身物品在几个女工脚下践踏。看着自己的毕业证书被踩破，盛夏终于有种走投无路的绝望。</p><p>哪里会有什么小偷？</p><p>只是她们不想她住在这里而已，排除异己，这是人类的本能。是她太笨，总对不相关的人抱以信心。</p><p>比如朱莉，比如程悦，比如这群女工……</p><p>可是，连自己的妈妈，都会嫌她多余，将她抛弃。连自己的爸爸，都会觉得她是个累赘，要将她卖掉。她又怎么能够去相信一群外人呢？</p><p>可笑至极！</p><p>这一次，眼睛终于承载不住她心里的酸涩，很多很多年都没有掉下来过的泪水，挣脱开她的眼皮，忽然如断了线的珠子，哗啦啦全部落了下来。</p><p>瞬间，盛夏就哭成了一个泪人儿。</p><p>但，软弱显然对喜欢落井下石的人并不管用。</p><p>“装哑巴？你以为不做声就没事了？这几点猫尿，对着隔壁那群好色的男人们还管点用。准备怎么着，报警还是赔钱！”</p><p>阿玲又是一个拖拽，逼盛夏抬头对上她的视线。</p><p>此刻，盛夏死了的心都有，哪里管她狗嘴里吐出来的是什么象牙。就这样由着她去吧。</p><p>反正，她全身上下就剩500块，反正，她对这一切已经失望透顶。一个女人，混到她这个份上，大不了也就是一死了之了。</p><p>盛夏气急，掏出兜里的钱都甩了出去。五张轻飘飘的一百块，雪花一样在眼前飞起来。</p><p>“栽赃诬陷，侵犯他人隐私。你确定报了警你还能出来？”</p><p>而就在气急败坏、绝望透顶之际，忽然就从门边插进来了一道男声。</p><p>低沉冷冽，如一阵冰霜刮过。屋子里原本吵吵嚷嚷的女工们都因为这突兀的声音停了下来，齐刷刷地望向门口。</p><p>盛夏泪眼朦胧的跟着循声看过去。却一抬眼，就对上了一双锐利冰冷的眼。</p>
